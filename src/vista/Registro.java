    public void validar(){
        
        String correo = txtCorreo.getText();
        String pass = String.valueOf(txtPass.getPassword());
        String nom = txtNombre.getText();
        String rol = cbxRol.getSelectedItem().toString();
        if(!"".equals(correo) ||  !"".equals(pass) ||  !"".equals(nom)){
            
            lg.setCorreo(correo);
            lg.setNombre(nom);
            lg.setPass(pass);
            lg.setRol(rol);
           boolean registrado=login.registrar(lg);
             
          if(registrado ==  true){
              
                JOptionPane.showMessageDialog(null,"Nuevo usuario creado");
          }else{
              
                JOptionPane.showMessageDialog(null,"Hubo un error al cargar el usuario");
          }
            dispose();
           
        }else{
                JOptionPane.showMessageDialog(null,"correo o contrase√±a incorrecta");
            }
    }

